package wwmm.crystaleye;

public class JournalDetails {

	private String publisherTitle;
	private String publisherAbbreviation;
	private String journalTitle;
	private String journalAbbreviation;
	
	public String getPublisherTitle() {
		return publisherTitle;
	}
	public void setPublisherTitle(String publisherTitle) {
		this.publisherTitle = publisherTitle;
	}
	public String getPublisherAbbreviation() {
		return publisherAbbreviation;
	}
	public void setPublisherAbbreviation(String publisherAbbreviation) {
		this.publisherAbbreviation = publisherAbbreviation;
	}
	public String getJournalTitle() {
		return journalTitle;
	}
	public void setJournalTitle(String journalTitle) {
		this.journalTitle = journalTitle;
	}
	public String getJournalAbbreviation() {
		return journalAbbreviation;
	}
	public void setJournalAbbreviation(String journalAbbreviation) {
		this.journalAbbreviation = journalAbbreviation;
	}
	
}
