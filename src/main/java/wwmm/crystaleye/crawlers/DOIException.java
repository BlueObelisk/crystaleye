package wwmm.crystaleye.crawlers;

public class DOIException extends RuntimeException {

	private static final long serialVersionUID = -7665916963461146251L;

	public DOIException() {
		super();
	}

	public DOIException(String message, Throwable cause) {
		super(message, cause);
	}

	public DOIException(String message) {
		super(message);
	}

	public DOIException(Throwable cause) {
		super(cause);
	}
	
}
